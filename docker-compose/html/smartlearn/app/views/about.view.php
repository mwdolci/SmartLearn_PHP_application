<div class="about-content">
    <p>
        SmartLearn est une petite plateforme pensée pour rendre la gestion des cours simple et agréable.
        Ici, pas de complications inutiles : l’objectif est que chacun puisse trouver rapidement les
        informations dont il a besoin et s’inscrire facilement aux cours qui l’intéressent.
    </p>

    <h2>Pourquoi SmartLearn ?</h2>
    <p>
        L’idée derrière SmartLearn est de proposer un espace clair, lisible et facile à utiliser.
        Que vous consultiez les cours depuis un ordinateur ou un smartphone, tout est conçu pour
        que la navigation reste fluide et intuitive.
    </p>

    <h2>Ce que vous pouvez faire ici</h2>
    <ul>
        <li>Parcourir les cours proposés</li>
        <li>Découvrir les informations essentielles avant de vous inscrire</li>
        <li>Gérer vos inscriptions dans la section “Mes cours”</li>
        <li>Profiter d’une interface simple et adaptée aux petits écrans</li>
    </ul>

    <h2>Une plateforme pensée pour rester accessible</h2>
    <p>
        SmartLearn se concentre sur l’essentiel : des pages lisibles, des actions claires et une
        interface qui ne vous perd pas en route. L’objectif est que vous puissiez utiliser la
        plateforme sans vous poser de questions, même si vous la découvrez pour la première fois.
    </p>

    <h2>Une application qui évolue</h2>
    <p>
        SmartLearn continue de s’améliorer au fil du temps. De nouvelles idées et fonctionnalités
        pourront s’ajouter progressivement, toujours avec la même intention : garder une application
        simple, pratique et agréable à utiliser.
    </p>
</div>